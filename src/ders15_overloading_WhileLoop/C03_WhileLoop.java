package ders15_overloading_WhileLoop;

public class C03_WhileLoop {
    public static void main(String[] args) {

        // iki basamakli sayilari aralarinda bir bosluk olacak sekilde yanyana yazdirin

        for (int i = 10; i <= 99 ; i++) {

            System.out.print(i+" ");

        }

        System.out.println();

        int a = 10;

        while (a<100){
            System.out.print(a+" ");
            a++;
        }

    }
}
